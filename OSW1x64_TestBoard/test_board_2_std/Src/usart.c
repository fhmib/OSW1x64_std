static ring_buffer usart1_rb;
static ring_buffer usart1_wb;
static usart_tr_stu usart1_tr_s = {
    .rb       = &usart1_rb,
    .wb       = &usart1_wb,
};
usart_tr_stu *usart1_tr = &usart1_tr_s;

uint32_t serial_tx(const uint8_t *buf, uint32_t len)
{
  uint32_t txed = 0;

  while (rb_is_empty(usart1_tr->wb) && (TERMINAL_UART.Instance->SR & USART_SR_TXE) && (txed < len)) {
    TERMINAL_UART.Instance->DR = buf[txed++];
  }
  TERMINAL_UART.Instance->CR1 &= ~((uint32_t)USART_CR1_TXEIE); // disable TXEIE while populating the buffer
  while (txed < len) {
    if (rb_safe_insert(usart1_tr->wb, buf[txed])) {
      txed++;
     } else break;
  }
  if (!rb_is_empty(usart1_tr->wb)) {
    TERMINAL_UART.Instance->CR1 |= USART_CR1_TXEIE;
  }
  return txed;
}

uint16_t serial_available()
{
  return rb_full_count(usart1_tr->rb);
}

uint8_t serial_read()
{
  return rb_remove(usart1_tr->rb);
}

